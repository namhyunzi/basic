package variable;

public class Sample08 {

	public static void main(String[] args) {
		
		/*
		 * 정수타입과 문자타입간의 형변환
		 * 
		 *	char ch1 = 65;	// 유니코드 65번에 해당하는 문자값이 대입된다.
		 * 	
		 * 	int num1 = 'A';	// 문자 'A'의 유니코드 번호 65가 대입된다.
		 */

		char ch1 = 65;
		System.out.println(ch1);
		
		int num1 = 'A';
		System.out.println(num1);
	}

}
