package day1;

public class Sample01 {
	public static void main(String[] args) {
		System.out.println("안녕, 이클립스");
	
	}
}

/*
예약어 설명 
	package : 해당 클래스의 패키지명을 나타낸다.
	public : 해당 클래스나 메소드는 전체 공개된다.
	class : 해당파일이 자바의 설계도(클래스)임을 나타낸다.
	static : 해당 클래스나 메소드가 정적 클래스 혹은 정적 메소드임을 나타낸다.
	void : 해당 메소드는 반환값이 없는 메소드임을 나타낸다.
	
식별자
	day1 - 패키지 이름
	Sample01 - 클래스 이름
	main - 메소드 이름
	String - 클래스 이름
	args - 매개변수 이름
	System - 클래스 이름
	out - 프로퍼티 이름
	println - 메소드 이름
*/